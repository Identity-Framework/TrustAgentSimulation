package edu.ncat.webid.trustsimulation;

public class AgentTrustInformation {
	private double trust;
	private double transTrust;
	
	
	
	public double getTrust() {
		return trust;
	}
	
	public void setTrust(double trust) {
		this.trust = trust;
	}
	
	public double getTransTrust() {
		return transTrust;
	}
	
	public void setTransTrust(double transTrust) {
		this.transTrust = transTrust;
	}
	
	
}
